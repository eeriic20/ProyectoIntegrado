package vista;

public class Cadena {
	/**
	 * @since 25-05-2021
	 */
	private String nombre;

	/**
	 * Constructor sin parametros
	 */
	public Cadena() {
		
	}

	/**
	 * 
	 * @param nombre
	 */
	public Cadena(String nombre) {
		
		this.nombre = nombre;
	}

	/**
	 * 
	 * @return
	 */
	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
	
}
